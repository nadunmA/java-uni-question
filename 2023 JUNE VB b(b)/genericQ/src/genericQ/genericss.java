package genericQ;

public class genericss <T, U>{
	
	T first;
	U second;
	

	public genericss(T first, U second) {
		super();
		this.first = first;
		this.second = second;
	}



	public T getFirst() {
		return first;
	}


	public U getSecond() {
		return second;
	}





}
